function p(x){
    var t=x+'';
    if (x<10){t='0'+t;}
    return t;
}

function g(q,c){
    var ds =1+q*20;
    var dt=1+q*20+20;
    var s='';
    for (var i=ds;i<dt;i++){
        s+='<tr><th>'+p(c[i][0])+'</th>';
        for (var j=1;j<c[i].length;j++){
            s+='<td>'+p(c[i][j])+'</td>';
        }
        s+='</tr>';
    }  
    s+='</table>';   
    return s;
}

